    private void loadReceptionistId() {
        try{
            receptionist=ReceptionistDao.getNonRegisteredReceptionist();            
            if(receptionist.isEmpty()){
                JOptionPane.showMessageDialog(null, "No un-regestired receptionist present");
                btnAdd.setEnabled(false);
                return;
            }
            btnAdd.setEnabled(true);
            Set<String> keys=receptionist.keySet();
            Iterator<String> it=keys.iterator();
            jcbReceptionistId.removeAllItems();
            jcbReceptionistId.addItem("Choose ID");
            while(it.hasNext()){
                jcbReceptionistId.addItem(it.next());
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "DB Error!", "ERROR!", JOptionPane.INFORMATION_MESSAGE);
            e.printStackTrace();
        }
    }

    private int validateInput() {
       if(txtUserId.getText().isEmpty())
           return -1;
       else if(pfdPassword.getPassword().length<4 || txtRePassword.getText().length()<4)
           return 0;
       return 1;
    }

    private boolean checkPassword() {
        return String.valueOf(pfdPassword.getPassword()).equals(txtRePassword.getText());
    }

    private void clear() {
       jcbReceptionistId.setSelectedIndex(0);
       txtReceptionistName.setText("");
       txtUserId.setText("");
       pfdPassword.setText("");
       txtRePassword.setText("");
    }
