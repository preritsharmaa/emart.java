    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(jcbReceptionistId.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Select an Id to proceed");
            return;
        }
        if(validateInput()==-1){
            JOptionPane.showMessageDialog(null, "UserId cannot be left blank");
            return;
        }
        if(validateInput()==0){            
            JOptionPane.showMessageDialog(null, "Password must be 4 characters long");
            return;
        }
        if(!checkPassword()){
            JOptionPane.showMessageDialog(null, "Password Mismatch");
            return;
        }
        UserPojo u=new UserPojo(txtUserId.getText(),jcbReceptionistId.getSelectedItem().toString(), String.valueOf(pfdPassword.getPassword()), "Receptionist", txtReceptionistName.getText());
        try{
            if(!ReceptionistDao.addReceptionist(u)){
                JOptionPane.showMessageDialog(null, "Could Not Add Receptionist!", "UNSUCCESFULL!", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(null, "Receptionist Was Added!", "SUCCESFUL!", JOptionPane.INFORMATION_MESSAGE);
            clear();
            loadReceptionistId();
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "DB ERROR!", "WRROR!", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

