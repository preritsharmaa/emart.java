    private void loadEmpId() {
       try{
           List<String> emp=EmployeeDao.getAllEmpId();
           for(String empId:emp)
               jcbEmpId.addItem(empId);           
       }
        catch(SQLException e){
              JOptionPane.showMessageDialog(null, "DB Error!", "ERROR", JOptionPane.ERROR_MESSAGE);
              e.printStackTrace();
        }        
    }

    private void showEmpDetails(String empId) {
        try{
            EmployeePojo e=EmployeeDao.getEmpDetails(empId);
            txtEmpName.setText(e.getEmpName());
            txtSalary.setText(String.valueOf(e.getSalary()));
            txtJob.setText(e.getJob());
        }
        catch(SQLException e){
              JOptionPane.showMessageDialog(null, "DB Error!", "ERROR", JOptionPane.ERROR_MESSAGE);
              e.printStackTrace();
        }  
    }
    
      private void clear() {
        jcbEmpId.setSelectedIndex(0);
        txtJob.setText("");
        txtEmpName.setText("");
        txtSalary.setText("");
    }
