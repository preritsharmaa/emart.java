    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if(jcbEmpId.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Please select an ID!", "SELECT ID!", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        try{
            if(!EmployeeDao.deleteEmp(jcbEmpId.getSelectedItem().toString())){
                JOptionPane.showMessageDialog(null, "Employee not deleted!", "FAILED!", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            JOptionPane.showMessageDialog(null, "Employee was deleted!", "SUCCESSFUL!", JOptionPane.INFORMATION_MESSAGE);
            clear();
        }
        catch(SQLException e){
              JOptionPane.showMessageDialog(null, "DB Error!", "ERROR", JOptionPane.ERROR_MESSAGE);
              e.printStackTrace();
        }  
    }//GEN-LAST:event_btnRemoveActionPerformed

