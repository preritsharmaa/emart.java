    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        if(jcbReceptionistId.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null, "Select an Id to proceed");
            return;
        }
        if(JOptionPane.showConfirmDialog(null, "Are you sure you want to remove selected receptionist?", "REMOVE RECEPTIONIST!", JOptionPane.YES_NO_OPTION)==JOptionPane.YES_OPTION){
            try{
                if(!ReceptionistDao.removeReceptionist(jcbReceptionistId.getSelectedItem().toString())){
                    JOptionPane.showMessageDialog(null, "Could not delete receptionist");
                    return;
                } 
                JOptionPane.showMessageDialog(null, "Reception was deleted");
                loadReceptionistId();
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null, "DB ERROR!", "ERROR!", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

