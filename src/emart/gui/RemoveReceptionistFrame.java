    private void loadReceptionistId() {
        try{
           List<String> receptionist=ReceptionistDao.getRegisteredReceptionistId();
           if(receptionist.isEmpty()){
               JOptionPane.showMessageDialog(null, "No Receptionist Present");
               btnRemove.setEnabled(false);
               return;
           }
           btnRemove.setEnabled(true);
           jcbReceptionistId.addItem("Choose ID:-");
           for(String id:receptionist){
               jcbReceptionistId.addItem(id);
           }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "DB ERROR!", "ERROR!", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }
