    private void btnGenerateBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateBillActionPerformed
        try{
            String oId=OrderDao.getNextOrderId();
            if(OrderDao.addOrderId(al, UserProfile.getUserid(), oId) && ProductDao.updateStocks(al)){
                JOptionPane.showMessageDialog(null, "Order with order Id:- "+oId+" generated successfully!", "SUCCESSFULL!", JOptionPane.INFORMATION_MESSAGE);
                btnReduce.setEnabled(false);
                btnRemove.setEnabled(false);  
                btnClear.setEnabled(false);
                btnGenerateBill.setEnabled(false);
                if(cbOrderFrame.isSelected())
                    changeFrame(new ReceptionistViewOrdersFrame());
                clear();
                return;
            }
            JOptionPane.showMessageDialog(null, "Error in placing order!", "UNSUCCESSFULL!", JOptionPane.INFORMATION_MESSAGE);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "DB ERROR!", "ERROR", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGenerateBillActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int row=jtProduct.getSelectedRow();
        setGrandTotal(-(Double.parseDouble(jtProduct.getValueAt(row, 7).toString())));
        tm.removeRow(row);
        btnReduce.setEnabled(false);
        btnRemove.setEnabled(false);
        if (tm.getRowCount()==0)
                clear();
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnReduceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReduceActionPerformed
        int row=jtProduct.getSelectedRow();
        int quantity=Integer.parseInt(jtProduct.getValueAt(row, 5).toString());
        if (quantity==1){
            setGrandTotal(-(Double.parseDouble(jtProduct.getValueAt(row, 7).toString())));
            tm.removeRow(row);
            btnReduce.setEnabled(false);
            btnRemove.setEnabled(false);
            if (tm.getRowCount()==0)
               clear();
            return;
        }
        String stax=jtProduct.getValueAt(row, 6).toString();
        int len=stax.length();
        int tax=Integer.parseInt(stax.substring(0, len-1)); 
        Double ourPrice=Double.parseDouble(jtProduct.getValueAt(row, 4).toString());
        quantity--;
        ProductPojo p=new ProductPojo();
        p.setTax(tax);
        p.setOurPrice(ourPrice);
        p.setQuantity(quantity);
        updateProductTotal(row, p, -1);
        tm.setValueAt(quantity, row, 5);
    }//GEN-LAST:event_btnReduceActionPerformed

