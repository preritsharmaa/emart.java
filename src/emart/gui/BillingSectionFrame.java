    private void loadItemList(String pId) {
        int row=checkPid(pId);
        if (row!=-1){
            ProductPojo p=al.get(row);
            int quantity=p.getQuantity()+1;
            p.setQuantity(quantity);
            tm.setValueAt(quantity, row, 5);
            updateProductTotal(row, p, 1);
            return;            
        }
        try{
            ProductPojo p=ProductDao.getProductDetails(pId);
            if(p.getProductId()==null){
                JOptionPane.showMessageDialog(null, "Please enter a valid Product ID");
                return;
            }
            al.add(p);
            rows=new Object[8];
            rows[0]=p.getProductId();
            rows[1]=p.getProductName();
            rows[2]=p.getProductCompany();
            rows[3]=p.getProductPrice();
            rows[4]=p.getOurPrice();
            rows[5]=p.getQuantity();
            rows[6]=p.getTax()+"%";
            rows[7]=p.getOurPrice()+(p.getTax()/100.0)*p.getOurPrice()*p.getQuantity();
            tm.addRow(rows);
            setGrandTotal(Double.parseDouble(rows[7].toString()));
            btnGenerateBill.setEnabled(true);
            btnClear.setEnabled(true);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "DB Error!", "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }

    private int checkPid(String pId) {
        for(int i=0; i<al.size(); i++){
            ProductPojo p=al.get(i);
            if(p.getProductId().equals(pId))
                return i;            
            }
        return -1;
    }

    private void setGrandTotal(double t) {        
        total+=t;
        lblTotal.setText("Total :-"+total);
    }

    private void updateProductTotal(int row, ProductPojo p, int flag) {
        Double total=(p.getOurPrice()+(p.getTax()/100.0)*p.getOurPrice())*p.getQuantity();
        tm.setValueAt(total, row, 7);
        if(flag==-1)
            setGrandTotal(-(total/p.getQuantity()));
        else
            setGrandTotal(total/p.getQuantity());
    }

    private void clear() {
         txtPId.setText("");
         tm.setRowCount(0);
         al.clear();
         total=0;
         setGrandTotal(0); 
         btnGenerateBill.setEnabled(false);
         btnClear.setEnabled(false);
    }
   
