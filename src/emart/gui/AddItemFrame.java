    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(!validateInput()){
            JOptionPane.showMessageDialog(null, "Please fill all the fields");
            return;
        }
        int n=jcbTax.getSelectedItem().toString().length();
        int tax=Integer.parseInt(jcbTax.getSelectedItem().toString().substring(0, n-1));
        ProductPojo p=new ProductPojo();
        p.setProductId(txtPId.getText());
        p.setProductCompany(txtPComp.getText().trim());
        p.setProductName(txtPName.getText().trim());
        p.setProductPrice(Double.parseDouble(txtPPrice.getText().trim()));
        p.setOurPrice(Double.parseDouble(txtOPrice.getText().trim()));
        p.setQuantity(Integer.parseInt(txtQuantity.getText().trim()));
        p.setTax(tax);
        try{
            if(!ProductDao.addProduct(p)){
                JOptionPane.showMessageDialog(null, "Could not add item");
                return;
            }
            JOptionPane.showMessageDialog(null, "Item added successfully");
            BarCode_IMG_Generator.createImage(txtPId.getText()+".png", txtPId.getText());
            clear();
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please input digits only in price and quantatiy");
            e.printStackTrace();
        }               
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "DB Error!", "ERROR!", JOptionPane.INFORMATION_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddActionPerformed

