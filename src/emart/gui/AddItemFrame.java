    private void loadNextId() {
        try{
            String pId=ProductDao.getNextProductId();
            txtPId.setText(pId);
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "DB Error!", "ERROR!", JOptionPane.INFORMATION_MESSAGE);
            e.printStackTrace();
        }
    }   

    private boolean validateInput() {
        return !(txtPName.getText().isEmpty() || txtPComp.getText().isEmpty() || txtPPrice.getText().isEmpty() || txtOPrice.getText().isEmpty() || txtQuantity.getText().isEmpty());
    }

    private void clear() {
        loadNextId();
        jcbTax.setSelectedIndex(0);
        txtOPrice.setText("");
        txtPComp.setText("");
        txtPName.setText("");
        txtPPrice.setText("");
        txtQuantity.setText("");
    }
